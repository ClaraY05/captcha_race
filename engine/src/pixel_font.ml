let width s ~scale =
  match String.length s with
  | 0 -> 0
  | n -> ((n * cell_w) + ((n - 1) * gap)) * scale
;;

let foreach_pixel s ~scale ~x ~y ~f =
  String.iteri s ~f:(fun i c ->
    let ox = x + (i * (cell_w + gap) * scale) in
    List.iteri (glyph c) ~f:(fun r row ->
      String.iteri row ~f:(fun col ch ->
        match Char.equal ch '#' with
        | false -> ()
        | true ->
          f
            ~x:(ox + (col * scale))
            ~y:(y + ((cell_h - 1 - r) * scale))
            ~size:scale)))
;;
